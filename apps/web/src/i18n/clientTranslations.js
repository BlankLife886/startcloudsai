function traditionalize(text) {
  if (TW_PHRASES[text]) return TW_PHRASES[text]
  return Array.from(text, (char) => TW_CHARS[char] || char).join('')
}

function preserveWhitespace(source, translated) {
  const leading = source.match(/^\s*/)?.[0] || ''
  const trailing = source.match(/\s*$/)?.[0] || ''
  return leading + translated + trailing
}

export function translateClientText(source, locale) {
  const text = String(source || '')
  const key = text.trim()
  if (!key || locale === 'zh-CN') return text
  if (locale === 'zh-TW') return preserveWhitespace(text, traditionalize(key))
  const exact = EN[key]
  if (exact) return preserveWhitespace(text, exact)
  if (key.includes(' - ')) {
    const parts = key.split(' - ')
    const translated = parts.map((part) => EN[part] || part)
    if (translated.some((part, index) => part !== parts[index])) {
      return preserveWhitespace(text, translated.join(' - '))
    }
  }

  const patterns = [
    [/^进行中\s*(\d+)$/, 'Running $1'],
    [/^成功\s*(\d+)\s*\/\s*失败\s*(\d+)$/, 'Succeeded $1 / Failed $2'],
    [/^第\s*(\d+)\/(\d+)\s*张$/, 'Image $1 of $2'],
    [/^(\d+)\s*张$/, '$1 images'],
    [/^(\d+)\s*张作品$/, '$1 artworks'],
    [/^(\d+)\s*条记录$/, '$1 history items'],
    [/^(¥[\d.]+)\s*\/\s*张$/, '$1 / image'],
    [/^(\d+)\/(\d+)\s*格$/, '$1 of $2 cells'],
    [/^(\d+)\s*格$/, '$1 cells'],
    [/^可选\s*·\s*(\d+)\s*张$/, 'Optional · up to $1 images'],
    [/^已收录\s*(\d+)\s*件$/, '$1 artworks'],
    [/^全部\s*(\d+)$/, 'All $1'],
    [/^已上线\s*(\d+)$/, 'Available $1'],
    [/^筹备中\s*(\d+)$/, 'Coming soon $1'],
    [/^(\d+)\s*项结果$/, '$1 results'],
    [/^(\d+)\s*\/\s*(\d+)\s*项$/, '$1 / $2 items'],
    [/^加载更多$/, 'Load more'],
    [/^生成耗时\s*(.+)$/, 'Generated in $1'],
    [/^注册于\s*(.+)$/, 'Joined $1'],
    [/^主色\s*(#[0-9a-f]{3,8})$/i, 'Brand color $1'],
  ]
  for (const [pattern, replacement] of patterns) {
    if (pattern.test(key)) return preserveWhitespace(text, key.replace(pattern, replacement))
  }
  return text
}

export function translateClientAttribute(source, locale) {
  return translateClientText(source, locale)
}
